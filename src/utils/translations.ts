export function translateProduct(product: any, lang: AppLanguage): any {
  if (lang === 'zh') return product;

  const translatedSku = translateSku(product.sku, lang);

  // 1. Is it a preloaded core product?
  if (CORE_PRODUCT_TRANSLATIONS[product.id]) {
    const coreTrans = CORE_PRODUCT_TRANSLATIONS[product.id][lang as Exclude<AppLanguage, 'zh'>] || CORE_PRODUCT_TRANSLATIONS[product.id]['en'];
    const catTrans = CATEGORIES_MAP[product.category]?.[lang] || product.category;
    return {
      ...product,
      sku: translatedSku,
      name: coreTrans.name,
      description: coreTrans.desc,
      category: catTrans
    };
  }

  // 2. Custom check for dynamic special perfumes
  if (product.category === '香水' || product.category === '奢享沙龙香') {
    const perfumeTrans = PERFUME_TRANSLATIONS[product.name];
    if (perfumeTrans) {
      const transObj = perfumeTrans[lang as Exclude<AppLanguage, 'zh'>] || perfumeTrans['en'];
      return {
        ...product,
        sku: translatedSku,
        name: transObj.name,
        description: transObj.desc,
        category: CATEGORIES_MAP[product.category]?.[lang] || product.category
      };
    }
  }

  // 3. Custom check for dynamic home appliances (家用电器)
  if (product.category === '家用电器') {
    const chineseIndex = product.name.search(/[\u4e00-\u9fa5]/);
    let englishPrefix = product.name;
    if (chineseIndex !== -1) {
      englishPrefix = product.name.substring(0, chineseIndex).trim();
    }

    let transName = englishPrefix;
    if (lang === 'en') {
      transName = englishPrefix;
    } else if (lang === 'es') {
      transName = `${englishPrefix} (Premium)`;
    } else if (lang === 'ja') {
      transName = `${englishPrefix} 【プレミアム仕様】`;
    } else if (lang === 'ko') {
      transName = `${englishPrefix} (프리미엄)`;
    } else if (lang === 'vi') {
      transName = `${englishPrefix} cao cấp`;
    }

    const descTemplates: Record<Exclude<AppLanguage, 'zh'>, string> = {
      en: `This ${englishPrefix} represents a profound fusion of smart technology and modern luxury living. Crafted from premium materials with advanced microchip controls, it delivers an unparalleled high-end experience in your daily life.`,
      es: `Este ${englishPrefix} representa una fusión indisoluble de tecnología inteligente y estética moderna del hogar. Diseñado con materiales nobles para brindar el máximo confort premium.`,
      ja: `この「${englishPrefix}」は、スマートテクノロジーと現代ラグジュアリーな美学が融合した最高の一品。先進のシステムコントロールと極上の職人技が宿る、最高品質。`,
      ko: `본 「${englishPrefix}」 에디션은 지능형 스마트 기술과 세련된 현대 가구 미학의 절묘한 융합으로 전례 없는 하이엔드 라이프스타일을 완성해 드립니다.`,
      vi: `Thiết bị ${englishPrefix} cao cấp này đại diện cho sự giao thoa hoàn mỹ giữa công nghệ thông minh và thẩm mỹ sống cao quý thượng lưu.`
    };
    const transDesc = descTemplates[lang as Exclude<AppLanguage, 'zh'>] || descTemplates['en'];

    return {
      ...product,
      sku: translatedSku,
      name: transName,
      description: transDesc,
      category: CATEGORIES_MAP[product.category]?.[lang] || product.category
    };
  }

  // 4. Fallback parser for dynamically generated 1000 items (pattern name: NOUN + STYLE)
  let translatedName = product.name;
  let translatedDesc = product.description;
  const translatedCat = CATEGORIES_MAP[product.category]?.[lang] || product.category;

  // Discover if this product name is generated via custom noun structure
  let matchedNoun = '';
  for (const n of ZH_NOUNS) {
    if (product.name.startsWith(n)) {
      matchedNoun = n;
      break;
    }
  }

  if (matchedNoun) {
    const remainder = product.name.substring(matchedNoun.length); // Should correspond to a style name
    const nounTrans = NOUN_MAP[matchedNoun]?.[lang] || NOUN_MAP[matchedNoun]?.['en'] || matchedNoun;
    const styleTrans = STYLES_MAP[remainder]?.[lang] || STYLES_MAP[remainder]?.['en'] || remainder;

    // Formatting based on language syntax
    if (lang === 'en') {
      translatedName = `${nounTrans} (${styleTrans})`;
    } else if (lang === 'es') {
      translatedName = `${nounTrans} - Edición ${styleTrans}`;
    } else if (lang === 'ja') {
      translatedName = `${nounTrans} 【${styleTrans}】`;
    } else if (lang === 'ko') {
      translatedName = `${nounTrans} / ${styleTrans}`;
    } else if (lang === 'vi') {
      translatedName = `${nounTrans} - ${styleTrans}`;
    }
  }

  // Description format:
  // 此款${noun}${style}是${adj}匠心力作。甄选卓越材质，精细融合现代与经典，为高阶美学生活家量身定制。
  // Match check to translate descriptions to perfect high-luxury equivalents on-the-fly!
  let matchedAdj = '';
  for (const adj of Object.keys(ADJ_MAP)) {
    if (product.description.includes(adj)) {
      matchedAdj = adj;
      break;
    }
  }

  if (matchedNoun && matchedAdj) {
    const nounTrans = NOUN_MAP[matchedNoun]?.[lang] || NOUN_MAP[matchedNoun]?.['en'] || matchedNoun;
    const styleTrans = STYLES_MAP[product.name.substring(matchedNoun.length)]?.[lang] || STYLES_MAP[product.name.substring(matchedNoun.length)]?.['en'] || '';
    const adjTrans = ADJ_MAP[matchedAdj]?.[lang] || ADJ_MAP[matchedAdj]?.['en'] || matchedAdj;

    if (lang === 'en') {
      translatedDesc = `This ${nounTrans} (${styleTrans}) is characterized by ${adjTrans} and master-crafted precision. Created using premium materials elegantly fusing classic and contemporary lines tailored for connoisseurs of the luxury aesthetic.`;
    } else if (lang === 'es') {
      translatedDesc = `Esta pieza ${nounTrans} (${styleTrans}) transmite una gran dosis de ${adjTrans}. Elaborada con materiales nobles que combinan a la perfección lo clásico y lo contemporáneo para los hogares de estética más selecta.`;
    } else if (lang === 'ja') {
      translatedDesc = `こちらの「${nounTrans}」${styleTrans}は、細部まで${adjTrans}職人技が光る名品。最高峰の素材のみを使用し、クラシックとコンテンポラリーの境界を優しく崩す、最高峰の美学。`;
    } else if (lang === 'ko') {
      translatedDesc = `본 「${nounTrans}」 ${styleTrans} 에디션은 ${adjTrans} 장인의 정성과 세련된 미학이 깃들어 있습니다. 엄선된 최상급 자재만을 활용하여 역사와 트렌디함을 정교하게 아울렀습니다.`;
    } else if (lang === 'vi') {
      translatedDesc = `Mẫu sản phẩm ${nounTrans} (${styleTrans}) này mang lại ${adjTrans} đầy chất nghệ thuật của nghệ nhân. Được tinh tuyển từ nguồn chất liệu thặng dư cao cấu nối liền giữa cổ điển và tối giản hiện đại.`;
    }
  }

  return {
    ...product,
    sku: translatedSku,
    name: translatedName,
    description: translatedDesc,
    category: translatedCat
  };
}
