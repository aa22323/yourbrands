export function translateSku(sku: string, lang: AppLanguage): string {
  if (lang === 'zh') return sku;
  const parts = sku.split('-');
  if (parts.length === 3) {
    const catPart = parts[0];
    const nounPart = parts[1];
    const numPart = parts[2];
    if (SKU_CAT_MAP[catPart] && SKU_NOUN_MAP[nounPart]) {
      return `${SKU_CAT_MAP[catPart]}-${SKU_NOUN_MAP[nounPart]}-${numPart}`;
    }
  }
  return sku;
}

